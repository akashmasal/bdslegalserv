        </p>

        <p>Below are some of the few points to keep in mind about ISO:</p>
        <ol>
            <li>The size of the organization does not matter</li>
            <li>You may be a restaurant or a consultancy or a government entity, your industry does not matter.</li>
            <li>ISO is not a standard of products. The quality of the products is not defined by ISO.</li>
            <li>It is also not a persona standard meaning an individual cannot be ISO certified.</li>
            <li>ISO is not a membership group.</li>
